def caesarCipher(s, k):
    # Write your code here
    alphabet = ['a','b','c','d','e','f','g','h','i','j','k','l','m','n','o','p','q','r','s','t','u','v','w','x','y','z']
    ans = ''
    for a in s:
        rotated = a
        if a.isalpha():
            if a.isupper():
                rotated = alphabet[(alphabet.index(a.lower()) + k)%26].upper()
            else:
                rotated = alphabet[(alphabet.index(a) + k)%26]
        ans = ans + rotated
    return ans

